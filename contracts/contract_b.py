def validate_contract_b(data: Dict[str, Any]) -> Dict[str, Any]:
    """
    Validate that `data` conforms to the Contract B schema (Vulnerability Manifest).

    All applicable validation rules (B-002 through B-008; B-001 is left to
    the orchestrator, which has Contract A available for cross-reference)
    are checked in a single pass; every problem found is collected rather
    than raising on the first failure.

    Args:
        data: The parsed payload (e.g. from analyzer.py's JSON output).

    Returns:
        A cleaned/normalized copy of `data`, with missing `confidence`
        values defaulted to DEFAULT_CONFIDENCE.

    Raises:
        ValidationError: If `data` does not conform to the Contract B
            schema. The exception's `errors` attribute holds the full
            list of individual problems found.
    """
    errors: List[str] = []

    if not isinstance(data, dict):
        raise ValidationError(
            f"Contract B payload must be an object, got {type(data).__name__}"
        )

    # contract_version
    contract_version = data.get("contract_version")
    if not isinstance(contract_version, str) or not CONTRACT_VERSION_PATTERN.match(
        contract_version
    ):
        errors.append(
            "contract_version: must match pattern "
            f"'{CONTRACT_VERSION_PATTERN.pattern}' (got {contract_version!r})"
        )

    # project_hash
    project_hash = data.get("project_hash")
    if not isinstance(project_hash, str) or not PROJECT_HASH_PATTERN.match(project_hash):
        errors.append(
            "project_hash: must be a 64-character lowercase SHA-256 hex "
            f"digest (got {project_hash!r})"
        )

    # generated_at
    generated_at = data.get("generated_at")
    if not isinstance(generated_at, str) or not ISO8601_PATTERN.match(generated_at):
        errors.append(
            "generated_at: must be an ISO 8601 UTC timestamp "
            f"(got {generated_at!r})"
        )

    # analysis_metadata (B-008)
    cleaned_metadata = _validate_analysis_metadata(
        data.get("analysis_metadata"), errors)

    # findings (B-002, B-003, B-004, B-005, B-006, B-007)
    findings = data.get("findings")
    if not isinstance(findings, list):
        errors.append(
            "findings: required field must be a list of finding objects")
        findings = []

    if len(findings) > MAX_FINDINGS:
        errors.append(
            f"findings array exceeds maximum of {MAX_FINDINGS} items "
            f"(got {len(findings)}) (rule B-006)"
        )

    seen_ids: set = set()
    cleaned_findings: List[Dict[str, Any]] = []
    for i, finding in enumerate(findings):
        cleaned_finding = _validate_finding(finding, i, seen_ids, errors)
        if cleaned_finding is not None:
            cleaned_findings.append(cleaned_finding)

    # request_metadata (optional)
    cleaned_request_metadata = _validate_request_metadata(
        data.get("request_metadata"), errors
    )

    if errors:
        raise ValidationError(errors)

    cleaned_data = dict(data)
    cleaned_data["contract_version"] = contract_version
    cleaned_data["project_hash"] = project_hash
    cleaned_data["generated_at"] = generated_at
    cleaned_data["analysis_metadata"] = cleaned_metadata
    cleaned_data["findings"] = cleaned_findings
    if "request_metadata" in data:
        cleaned_data["request_metadata"] = cleaned_request_metadata
    return cleaned_data


def is_valid_contract_b(data: Dict[str, Any]) -> bool:
    """Return True if `data` is a valid Contract B payload, False otherwise."""
    try:
        validate_contract_b(data)
        return True
    except ValidationError:
        return False


def generate_finding_id(vulnerability_class: str, index: int) -> str:
    """
    Generate a finding ID in the format "FSA-XXXXXX-001".

    The class segment is a short, stable abbreviation of
    `vulnerability_class` (see `_VULN_CLASS_ABBREVIATIONS`); unknown
    classes fall back to their first 6 alphanumeric characters,
    uppercased. The sequence segment is `index` zero-padded to at
    least 3 digits.

    Args:
        vulnerability_class: One of the values in VULNERABILITY_CLASSES
            (e.g. "BROKEN_AUTH"). Unrecognized values are still accepted
            and abbreviated on a best-effort basis.
        index: A 1-based sequence number for this finding within the
            current run (e.g. 1, 2, 3...).

    Returns:
        A string such as "FSA-BAUTH-001".
    """
    if vulnerability_class in _VULN_CLASS_ABBREVIATIONS:
        abbreviation = _VULN_CLASS_ABBREVIATIONS[vulnerability_class]
    else:
        alnum_only = re.sub(r"[^A-Za-z0-9]", "", vulnerability_class or "")
        abbreviation = (alnum_only[:6] or "UNKNOWN").upper()

    sequence = max(index, 0)
    return f"FSA-{abbreviation}-{sequence:03d}"


def get_contract_b_schema() -> Dict[str, Any]:
    """Return the JSON Schema (draft-07) representation of Contract B."""
    return {
        "$schema": "http://json-schema.org/draft-07/schema#",
        "title": "ContractB_VulnerabilityManifest",
        "description": (
            "Vulnerability Manifest produced by the FSA (analyzer.py) and "
            "consumed by the OSE Server (main.py)."
        ),
        "type": "object",
        "required": [
            "contract_version",
            "project_hash",
            "generated_at",
            "analysis_metadata",
            "findings",
        ],
        "properties": {
            "contract_version": {
                "type": "string",
                "pattern": CONTRACT_VERSION_PATTERN.pattern,
            },
            "project_hash": {
                "type": "string",
                "pattern": PROJECT_HASH_PATTERN.pattern,
            },
            "generated_at": {
                "type": "string",
                "pattern": ISO8601_PATTERN.pattern,
                "description": "ISO 8601 UTC timestamp.",
            },
            "analysis_metadata": {
                "type": "object",
                "required": list(REQUIRED_METADATA_FIELDS),
                "properties": {
                    "scanner_version": {"type": "string"},
                    "files_analyzed": {"type": "integer", "minimum": 0},
                    "analysis_duration_seconds": {"type": "number", "minimum": 0},
                    "target_tracks": {
                        "type": "array",
                        "minItems": 1,
                        "items": {"type": "string", "enum": TRACKS},
                        "description": "Rule B-008: must be non-empty.",
                    },
                },
            },
            "findings": {
                "type": "array",
                "maxItems": MAX_FINDINGS,
                "items": {
                    "type": "object",
                    "required": list(REQUIRED_FINDING_FIELDS),
                    "properties": {
                        "id": {
                            "type": "string",
                            "pattern": FINDING_ID_PATTERN.pattern,
                        },
                        "file_path": {"type": "string"},
                        "line_start": {"type": "integer", "minimum": 1},
                        "line_end": {
                            "type": "integer",
                            "description": "Must be >= line_start (rule B-002).",
                        },
                        "vulnerability_class": {
                            "type": "string",
                            "enum": VULNERABILITY_CLASSES,
                        },
                        "severity": {"type": "string", "enum": SEVERITY_LEVELS},
                        "code_snippet": {
                            "type": "string",
                            "maxLength": MAX_CODE_SNIPPET_LENGTH,
                            "description": (
                                "Must not contain absolute paths or "
                                "user-specific information (rule B-003)."
                            ),
                        },
                        "description": {
                            "type": "string",
                            "maxLength": MAX_DESCRIPTION_LENGTH,
                        },
                        "fix_principle": {
                            "type": "string",
                            "maxLength": MAX_FIX_PRINCIPLE_LENGTH,
                        },
                        "confidence": {
                            "type": "number",
                            "minimum": 0.0,
                            "maximum": 1.0,
                            "default": DEFAULT_CONFIDENCE,
                        },
                        "false_positive_risk": {
                            "type": "string",
                            "enum": FALSE_POSITIVE_RISK_LEVELS,
                        },
                        "related_finding_ids": {
                            "type": "array",
                            "items": {"type": "string"},
                        },
                    },
                },
            },
            "request_metadata": {
                "type": "object",
                "properties": {
                    "client_version": {"type": "string"},
                    "user_id": {"type": "string"},
                    "preferred_model": {
                        "type": "string",
                        "enum": PREFERRED_MODELS,
                    },
                },
            },
        },
    }
