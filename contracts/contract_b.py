def _contains_absolute_path(snippet: str) -> bool:
    """Return True if `snippet` appears to leak an absolute filesystem path."""
    if any(indicator in snippet for indicator in _ABSOLUTE_PATH_INDICATORS):
        return True
    return _WINDOWS_DRIVE_PATTERN.search(snippet) is not None


def _validate_finding(
    finding: Any, index: int, seen_ids: set, errors: List[str]
) -> Optional[Dict[str, Any]]:
    """Validate a single findings[index] entry, appending problems to `errors`.

    Returns a cleaned copy of the finding (with a defaulted `confidence`
    where applicable), or None if the entry was not a dict at all.
    """
    if not isinstance(finding, dict):
        errors.append(
            f"findings[{index}]: expected an object, got {type(finding).__name__}"
        )
        return None

    cleaned = dict(finding)

    for required_field in REQUIRED_FINDING_FIELDS:
        if required_field not in finding:
            errors.append(
                f"findings[{index}].{required_field}: required field is missing"
            )

    # id (B-007 uniqueness, format)
    finding_id = finding.get("id")
    if not isinstance(finding_id, str) or not FINDING_ID_PATTERN.match(finding_id):
        errors.append(
            f"findings[{index}].id: must match pattern "
            f"'{FINDING_ID_PATTERN.pattern}' (got {finding_id!r})"
        )
    elif finding_id in seen_ids:
        errors.append(
            f"findings[{index}].id: duplicate id '{finding_id}' "
            "(rule B-007: ids must be unique within findings)"
        )
    else:
        seen_ids.add(finding_id)

    # file_path
    file_path = finding.get("file_path")
    if not isinstance(file_path, str) or not file_path:
        errors.append(
            f"findings[{index}].file_path: required non-empty string field "
            "is missing or invalid"
        )

    # line_start / line_end (B-002)
    line_start = finding.get("line_start")
    line_end = finding.get("line_end")
    valid_line_start = isinstance(line_start, int) and not isinstance(
        line_start, bool) and line_start >= 1
    valid_line_end = isinstance(
        line_end, int) and not isinstance(line_end, bool)
    if not valid_line_start:
        errors.append(
            f"findings[{index}].line_start: must be an integer >= 1 "
            f"(got {line_start!r})"
        )
    if not valid_line_end:
        errors.append(
            f"findings[{index}].line_end: must be an integer (got {line_end!r})"
        )
    if valid_line_start and valid_line_end and line_end < line_start:
        errors.append(
            f"findings[{index}].line_end: must be >= line_start "
            f"(got line_end={line_end}, line_start={line_start}) (rule B-002)"
        )

    # vulnerability_class (B-004)
    vuln_class = finding.get("vulnerability_class")
    if vuln_class not in VULNERABILITY_CLASSES:
        errors.append(
            f"findings[{index}].vulnerability_class: must be one of "
            f"{VULNERABILITY_CLASSES} (got {vuln_class!r}) (rule B-004)"
        )

    # severity
    severity = finding.get("severity")
    if severity not in SEVERITY_LEVELS:
        errors.append(
            f"findings[{index}].severity: must be one of {SEVERITY_LEVELS} "
            f"(got {severity!r})"
        )

    # code_snippet (B-003, length)
    code_snippet = finding.get("code_snippet")
    if not isinstance(code_snippet, str):
        errors.append(
            f"findings[{index}].code_snippet: required string field is "
            "missing or invalid"
        )
    else:
        if len(code_snippet) > MAX_CODE_SNIPPET_LENGTH:
            errors.append(
                f"findings[{index}].code_snippet: exceeds maximum length of "
                f"{MAX_CODE_SNIPPET_LENGTH} characters (got {len(code_snippet)})"
            )
        if _contains_absolute_path(code_snippet):
            errors.append(
                f"findings[{index}].code_snippet: must not contain absolute "
                "paths or user-specific information (rule B-003)"
            )

    # description
    description = finding.get("description")
    if not isinstance(description, str) or not description:
        errors.append(
            f"findings[{index}].description: required non-empty string "
            "field is missing or invalid"
        )
    elif len(description) > MAX_DESCRIPTION_LENGTH:
        errors.append(
            f"findings[{index}].description: exceeds maximum length of "
            f"{MAX_DESCRIPTION_LENGTH} characters (got {len(description)})"
        )

    # fix_principle
    fix_principle = finding.get("fix_principle")
    if not isinstance(fix_principle, str) or not fix_principle:
        errors.append(
            f"findings[{index}].fix_principle: required non-empty string "
            "field is missing or invalid"
        )
    elif len(fix_principle) > MAX_FIX_PRINCIPLE_LENGTH:
        errors.append(
            f"findings[{index}].fix_principle: exceeds maximum length of "
            f"{MAX_FIX_PRINCIPLE_LENGTH} characters (got {len(fix_principle)})"
        )

    # confidence (B-005, with default)
    if "confidence" not in finding or finding.get("confidence") is None:
        logger.warning(
            "findings[%d]: confidence is missing; defaulting to %.1f",
            index,
            DEFAULT_CONFIDENCE,
        )
        cleaned["confidence"] = DEFAULT_CONFIDENCE
    else:
        confidence = finding.get("confidence")
        is_number = isinstance(confidence, (int, float)
                               ) and not isinstance(confidence, bool)
        if not is_number or not (0.0 <= float(confidence) <= 1.0):
            errors.append(
                f"findings[{index}].confidence: must be between 0.0 and 1.0 "
                f"(got {confidence!r}) (rule B-005)"
            )
        else:
            cleaned["confidence"] = float(confidence)

    # optional: false_positive_risk
    if "false_positive_risk" in finding and finding["false_positive_risk"] is not None:
        if finding["false_positive_risk"] not in FALSE_POSITIVE_RISK_LEVELS:
            errors.append(
                f"findings[{index}].false_positive_risk: must be one of "
                f"{FALSE_POSITIVE_RISK_LEVELS} (got {finding['false_positive_risk']!r})"
            )

    # optional: related_finding_ids
    if "related_finding_ids" in finding and finding["related_finding_ids"] is not None:
        related = finding["related_finding_ids"]
        if not isinstance(related, list) or not all(isinstance(r, str) for r in related):
            errors.append(
                f"findings[{index}].related_finding_ids: must be an array "
                "of strings if present"
            )

    return cleaned


def _validate_analysis_metadata(metadata: Any, errors: List[str]) -> Optional[Dict[str, Any]]:
    """Validate the `analysis_metadata` object, appending problems to `errors`."""
    if not isinstance(metadata, dict):
        errors.append("analysis_metadata: required field must be an object")
        return None

    cleaned = dict(metadata)

    for required_field in REQUIRED_METADATA_FIELDS:
        if required_field not in metadata:
            errors.append(
                f"analysis_metadata.{required_field}: required field is missing"
            )

    scanner_version = metadata.get("scanner_version")
    if "scanner_version" in metadata and (
        not isinstance(scanner_version, str) or not scanner_version
    ):
        errors.append(
            "analysis_metadata.scanner_version: must be a non-empty string"
        )

    files_analyzed = metadata.get("files_analyzed")
    if "files_analyzed" in metadata and (
        not isinstance(files_analyzed, int)
        or isinstance(files_analyzed, bool)
        or files_analyzed < 0
    ):
        errors.append(
            "analysis_metadata.files_analyzed: must be a non-negative integer "
            f"(got {files_analyzed!r})"
        )

    duration = metadata.get("analysis_duration_seconds")
    if "analysis_duration_seconds" in metadata and (
        not isinstance(duration, (int, float))
        or isinstance(duration, bool)
        or duration < 0
    ):
        errors.append(
            "analysis_metadata.analysis_duration_seconds: must be a "
            f"non-negative number (got {duration!r})"
        )

    # target_tracks (B-008)
    target_tracks = metadata.get("target_tracks")
    if "target_tracks" in metadata:
        if not isinstance(target_tracks, list) or len(target_tracks) < 1:
            errors.append(
                "analysis_metadata.target_tracks: must be a non-empty array "
                "(rule B-008)"
            )
        else:
            invalid_tracks = [t for t in target_tracks if t not in TRACKS]
            if invalid_tracks:
                errors.append(
                    f"analysis_metadata.target_tracks: contains unsupported "
                    f"track(s) {invalid_tracks}; must be a subset of {TRACKS}"
                )

    return cleaned


def _validate_request_metadata(metadata: Any, errors: List[str]) -> Optional[Dict[str, Any]]:
    """Validate the optional `request_metadata` object, appending problems to `errors`."""
    if metadata is None:
        return None
    if not isinstance(metadata, dict):
        errors.append("request_metadata: must be an object if present")
        return None

    cleaned = dict(metadata)

    if "client_version" in metadata and not isinstance(metadata["client_version"], str):
        errors.append(
            "request_metadata.client_version: must be a string if present")

    if "user_id" in metadata and not isinstance(metadata["user_id"], str):
        errors.append("request_metadata.user_id: must be a string if present")

    if "preferred_model" in metadata:
        if metadata["preferred_model"] not in PREFERRED_MODELS:
            errors.append(
                "request_metadata.preferred_model: must be one of "
                f"{PREFERRED_MODELS} (got {metadata['preferred_model']!r})"
            )

    return cleaned


